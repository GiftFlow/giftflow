		<h2> Why we started GiftFlow </h2>
	
		
<p>At GiftFlow, we looked at the growing trend of wasteful overconsumption and saw
a situation that could benefit enormously from improved communication. 
Giving something away on GiftFlow both prevents waste and overconsumption. Users can avoid adding to the landfill while
also saving each other money. Communication is all that is required. 

As a GiftFlow user, by posting what you need and what you have 
to give away, you contribute to a growing database of gifts and needs. 
Each post creates the possibility for a successful gift. Each gift
prevents waste, reduces consumption and most importantly, earns the gratitude of the recipient.
</p>

<p>
We started GiftFlow to give communities a new set of tools. </p>

<p>
The driving force behind the system is an ethic of indirect reciprocity or circular giving.
Lewis Hyde described it best in his book <i>The Gift</i>:</p>

<p>"Circular giving differs from reciprocal giving in several ways. First, when the gift moves
in a circle, no one ever receives it from the same person she gives it to...When the gift moves 
in a circle its motion is beyond the control of the personal ego, and so each bearer must be part 
of the group and each donation is an act of social faith." </p>
<p>
Our team continues to grow. We are a non-profit and welcome the gifts and contributions of 
anyone who might be interested. We hope that the online community of GiftFlow creates an 
offline community of mutual interdependence and support.
</p>


