		<h4>Is GiftFlow a barter system?</h4>
		<p>When you give on GiftFlow, you're not exchanging Product A for Product B. While many sites do operate like this, we feel an exchange model is too limited. Instead, on GiftFlow, you participate in a circle of reciprocity. You give to one user and ask from another. Each gift is an act of faith that the community will provide for your needs in return.
		</p>
		
		<h4>Is GiftFlow is a points/credits system?</h4>
		<p>On some sites, you collect points or credits when you give, which you can then use to receive gifts from other users. Not on GiftFlow. We've chosen not to use the credits model because we honestly could not think of one that was not skewed or exploitable. If one of your needs matches another user's gift, the two of you meet in person and the gift is given. That's it! No exchanges, no points, nothing else; we want to make the gift-giving as organic and altruistic as possible.
		</p>

		<h4>Isn't GiftFlow just a replica of existing sites such as Freecycle?</h4>
		<p>Though GiftFlow is certainly similar to existing sites, our emphasis is really on community-building. We endeavor to be a social network of gift-giving, in which each user has a full profile that includes their interests and activities as well as more mundane necessities such as name and location. We aim to be a combination of CouchSurfing and Craigslist. We use the social-network and reputation system to enable a gift economy of all goods and services.  
		</p>
